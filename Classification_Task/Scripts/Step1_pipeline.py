def get_secret(name: str):
    """Load secret from Colab userdata first, then env."""
    if IN_COLAB:
        try:
            value = userdata.get(name)
            if value:
                return value
        except Exception:
            pass
    return os.getenv(name)


def get_client(model_key: str) -> OpenAI:
    cfg = MODEL_CONFIGS[model_key]
    api_key = get_secret(cfg["api_key_env"])

    if not api_key:
        raise ValueError(
            f"Missing API key for model '{model_key}'. "
            f"Expected in env/Colab secret: {cfg['api_key_env']}"
        )

    base_url = cfg.get("base_url")
    provider = cfg.get("provider", "unknown") # Get provider, default to unknown if not specified

    # If base_url is explicitly set, use it.
    if base_url:
        return OpenAI(api_key=api_key, base_url=base_url)

    # If no base_url, but it's an 'openai' provider, it can default to api.openai.com.
    if provider == "openai":
        return OpenAI(api_key=api_key)

    # If no base_url and not an 'openai' provider, it MUST have a base_url.
    # This prevents sending non-OpenAI keys to OpenAI's default endpoint.
    raise ValueError(
        f"Model '{model_key}' is configured with provider '{provider}' "
        "and requires a 'base_url' for its inference endpoint. "
        f"Please ensure 'QWEN_BASE_URL' (or similar based on the provider) is set "
        "in your environment or Colab secrets, or directly in MODEL_CONFIGS."
    )


def get_model_name(model_key: str) -> str:
    return MODEL_CONFIGS[model_key]["model"]


def find_text_column(df: pd.DataFrame) -> str:
    for col in TEXT_CANDIDATES:
        if col in df.columns:
            return col
    raise ValueError(
        f"No text column found. Tried: {TEXT_CANDIDATES}. "
        f"Available columns: {list(df.columns)}"
    )


def load_system_message(prompt_path: str) -> str:
    with open(prompt_path, "r", encoding="utf-8") as file:
        prompt_data = json.load(file)
    return prompt_data["SYSTEM_MESSAGE"]


def prepare_input_data(csv_path: str, path_df: str, sample_n=None):
    try:
        df_csv = pd.read_csv(csv_path, encoding="utf-8")
    except UnicodeDecodeError:
        df_csv = pd.read_csv(csv_path, encoding="latin1")

    if sample_n is not None:
        df_csv = df_csv.sample(n=min(sample_n, len(df_csv)), random_state=42).copy()

    feather.write_feather(df_csv, path_df)
    return df_csv


def build_user_content(article_group):
    """
    Build the user content for the API.
    article_group: list of dicts like {"id": 1, "text": "..."}
    """
    user_content = (
        "Classify the following parliamentary speech strictly per the system instructions. "
        "Respond with ONLY a JSON object of the form {\"items\": [ ... ]}, "
        f"where \"items\" is an array of objects (one per text, same order). "
        f"Each object MUST include keys: id, r (r in {VALID_R_VALUES}), b (b in {{true,false}}). "
        "No extra text, no explanations.\n\n"
    )

    for article in article_group:
        user_content += f"ID: {article['id']}\n"
        user_content += f"Text: {article['text']}\n\n"

    return user_content


def send_request(request_id, article_group, system_message, model_key, max_retries=3):
    """
    Send one request for one article_group.
    """
    client = get_client(model_key)
    model = get_model_name(model_key)
    user_content = build_user_content(article_group)

    body = {
        "model": model,
        "messages": [
            {"role": "system", "content": system_message},
            {"role": "user", "content": user_content},
        ],
        "max_tokens": 5000,
        "temperature": 0.1,
        "response_format": {
            "type": "json_schema",
            "json_schema": JSON_SCHEMA,
        },
    }

    for attempt in range(1, max_retries + 1):
        try:
            response = client.chat.completions.create(**body)
            print(f"[{model_key}] Request {request_id} completed successfully.")
            return {
                "request_id": request_id,
                "model_key": model_key,
                "status": "success",
                "response": response,
                "error": None,
            }
        except Exception as e:
            err = str(e)
            print(f"[{model_key}] Request {request_id} failed on attempt {attempt}: {err}")

            if attempt == max_retries:
                return {
                    "request_id": request_id,
                    "model_key": model_key,
                    "status": "failed",
                    "response": None,
                    "error": err,
                }

            time.sleep(min(2 ** attempt, 10))


def process_requests_in_parallel(grouped_articles, system_message, model_key, max_workers=8):
    """
    Send grouped requests in parallel and collect results.
    """
    results = []

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        future_to_meta = {
            executor.submit(
                send_request,
                request_id,
                article_group,
                system_message,
                model_key,
            ): (request_id, article_group)
            for request_id, article_group in enumerate(grouped_articles)
        }

        for future in as_completed(future_to_meta):
            request_id, article_group = future_to_meta[future]
            try:
                r = future.result()
            except Exception as e:
                r = {
                    "request_id": request_id,
                    "model_key": model_key,
                    "status": "failed",
                    "response": None,
                    "error": f"Thread execution error: {e}",
                }

            r["article_id"] = article_group[0]["id"]
            r["article_text"] = article_group[0]["text"]
            results.append(r)

    results.sort(key=lambda x: x["request_id"])
    return results


def _extract_content_from_response(response_obj):
    """
    Works for both OpenAI SDK objects and dict dumps.
    """
    try:
        return response_obj.choices[0].message.content
    except Exception:
        return response_obj["choices"][0]["message"]["content"]


def flatten_results(batch_results):
    flattened_data = []
    failed_data = []

    for result in batch_results:
        if result["status"] == "success" and result["response"] is not None:
            try:
                response_obj = result["response"]
                response_content = _extract_content_from_response(response_obj)

                if not response_content or not isinstance(response_content, str):
                    raise ValueError("Empty or non-string response content")

                data = json.loads(response_content)

                if isinstance(data, dict) and "items" in data:
                    json_list = data["items"]
                elif isinstance(data, list):
                    json_list = data
                elif isinstance(data, dict):
                    json_list = [data]
                else:
                    raise ValueError("Unexpected JSON top-level type")

                if not isinstance(json_list, list):
                    raise ValueError("items is not a list")

                for entry in json_list:
                    flattened_entry = {
                        "model_key": result["model_key"],
                        "request_id": result["request_id"],
                        "id": entry.get("id", result.get("article_id")),
                        "text": result.get("article_text"),
                        "r": entry.get("r"),
                        "b": entry.get("b"),
                    }
                    flattened_data.append(flattened_entry)

            except Exception as e:
                failed_data.append({
                    "model_key": result["model_key"],
                    "request_id": result["request_id"],
                    "article_id": result.get("article_id"),
                    "text": result.get("article_text"),
                    "error": f"Parse error: {e}",
                })

        else:
            failed_data.append({
                "model_key": result["model_key"],
                "request_id": result["request_id"],
                "article_id": result.get("article_id"),
                "text": result.get("article_text"),
                "error": result.get("error", "No response object"),
            })

    return pd.DataFrame(flattened_data), pd.DataFrame(failed_data)


def ensure_run_folders(model_key, resume_run_folder=None):
    """
    Create model-specific run folders.
    """
    if resume_run_folder:
        partial_folder = os.path.join(output_data_folder, resume_run_folder)
        final_folder = os.path.join(final_data_folder, resume_run_folder)
        print(f"[{model_key}] Resuming from folder: {partial_folder}")
    else:
        unique_run_id = f"{model_key}_{datetime.now().strftime('%Y%m%d_%H%M%S')}"
        partial_folder = os.path.join(output_data_folder, f"run_{unique_run_id}")
        final_folder = os.path.join(final_data_folder, f"run_{unique_run_id}")
        print(f"[{model_key}] Starting a new run in folder: {partial_folder}")

    os.makedirs(partial_folder, exist_ok=True)
    os.makedirs(final_folder, exist_ok=True)

    checkpoint_path = os.path.join(partial_folder, "checkpoint.txt")
    return partial_folder, final_folder, checkpoint_path


def run_classification_in_chunks(model_key, chunk_size=100, max_workers=8, resume_run_folder=None):
    print(f"\n========== Running model: {model_key} ==========")

    overall_start_time = time.time()
    system_message = load_system_message(prompt_path)

    run_partial_results_folder, run_final_results_folder, checkpoint_path = ensure_run_folders(
        model_key=model_key,
        resume_run_folder=resume_run_folder,
    )

    df = feather.read_feather(path_df)

    if ID_COL not in df.columns:
        raise ValueError(f"Missing ID column '{ID_COL}'")

    text_col = find_text_column(df)

    articles = [
        {"id": row[ID_COL], "text": row[text_col]}
        for _, row in df.iterrows()
    ]

    total_articles = len(articles)
    print(f"[{model_key}] Total articles to process: {total_articles}")

    cumulative_tokens = 0
    total_processed_articles = 0

    last_completed_chunk = 0
    if os.path.exists(checkpoint_path):
        try:
            with open(checkpoint_path, "r", encoding="utf-8") as cp:
                last_completed_chunk = int(cp.read().strip())
            print(f"[{model_key}] Resuming from chunk {last_completed_chunk + 1}")
        except Exception:
            print(f"[{model_key}] Invalid checkpoint file. Starting from the beginning.")
            last_completed_chunk = 0
    else:
        print(f"[{model_key}] No checkpoint file found. Starting from the beginning.")

    total_chunks = (total_articles - 1) // chunk_size + 1 if total_articles > 0 else 0

    for start_idx in range(0, total_articles, chunk_size):
        end_idx = min(start_idx + chunk_size, total_articles)
        chunk_index = (start_idx // chunk_size) + 1

        if chunk_index <= last_completed_chunk:
            print(f"[{model_key}] Chunk {chunk_index} already processed according to checkpoint. Skipping.")
            continue

        articles_in_chunk = articles[start_idx:end_idx]

        partial_json_path = os.path.join(
            run_partial_results_folder, f"batch_results_{chunk_index}.json"
        )
        partial_csv_path = os.path.join(
            run_partial_results_folder, f"flattened_results_{chunk_index}.csv"
        )
        partial_failed_path = os.path.join(
            run_partial_results_folder, f"failed_results_{chunk_index}.csv"
        )

        if os.path.exists(partial_json_path) and os.path.exists(partial_csv_path):
            print(f"[{model_key}] Chunk {chunk_index} already processed (files exist). Skipping.")
            with open(checkpoint_path, "w", encoding="utf-8") as cp:
                cp.write(str(chunk_index))
            continue

        grouped_articles = [[article] for article in articles_in_chunk]

        print(f"[{model_key}] Processing chunk {chunk_index} ({len(articles_in_chunk)} articles)...")
        batch_start_time = time.time()

        batch_results = process_requests_in_parallel(
            grouped_articles=grouped_articles,
            system_message=system_message,
            model_key=model_key,
            max_workers=max_workers,
        )

        serializable_results = []
        batch_tokens = 0

        for result in batch_results:
            if result["status"] == "success" and result["response"] is not None:
                response_data = result["response"].model_dump()
                batch_tokens += response_data.get("usage", {}).get("total_tokens", 0)
                result["response"] = response_data

            serializable_results.append(result)

        with open(partial_json_path, "w", encoding="utf-8") as f:
            json.dump(serializable_results, f, indent=2, ensure_ascii=False)

        flattened_df, failed_df = flatten_results(serializable_results)

        if not flattened_df.empty:
            flattened_df.to_csv(partial_csv_path, index=False, encoding="utf-8-sig", sep=";")
        else:
            print(f"[{model_key}] Warning: Flattened DataFrame is empty for chunk {chunk_index}.")

        if not failed_df.empty:
            failed_df.to_csv(partial_failed_path, index=False, encoding="utf-8-sig", sep=";")
            print(f"[{model_key}] Failed requests saved to: {partial_failed_path}")

        cumulative_tokens += batch_tokens
        total_processed_articles += len(articles_in_chunk)

        batch_elapsed_time = time.time() - batch_start_time
        articles_per_minute = (
            len(articles_in_chunk) / (batch_elapsed_time / 60)
            if batch_elapsed_time > 0 else 0
        )

        print(
            f"[{model_key}] Finished chunk {chunk_index}/{total_chunks}. "
            f"Batch tokens: {batch_tokens}. Total tokens: {cumulative_tokens}. "
            f"Elapsed: {batch_elapsed_time:.2f}s. Pace: {articles_per_minute:.2f} articles/min."
        )

        with open(checkpoint_path, "w", encoding="utf-8") as cp:
            cp.write(str(chunk_index))

    overall_elapsed_time = time.time() - overall_start_time
    print(f"[{model_key}] All chunks processed successfully! Total elapsed time: {overall_elapsed_time:.2f} seconds.")

    return run_partial_results_folder, run_final_results_folder


def merge_results(run_partial_results_folder, run_final_results_folder, model_key):
    """
    Merge all partial CSVs into one final CSV for one model.
    """
    partial_csv_files = sorted(
        f for f in os.listdir(run_partial_results_folder)
        if f.startswith("flattened_results_") and f.endswith(".csv")
    )

    partial_csv_paths = [os.path.join(run_partial_results_folder, f) for f in partial_csv_files]

    df_list = []
    for csv_file in partial_csv_paths:
        df_chunk = pd.read_csv(csv_file, encoding="utf-8-sig", sep=";")
        df_list.append(df_chunk)

    if df_list:
        merged_df = pd.concat(df_list, ignore_index=True)
        final_csv_path = os.path.join(run_final_results_folder, f"flattened_results_{model_key}.csv")
        merged_df.to_csv(final_csv_path, index=False, encoding="utf-8-sig", sep=";")
        print(f"[{model_key}] Final merged CSV saved at: {final_csv_path}")
        return final_csv_path
    else:
        print(f"[{model_key}] No partial CSV files found to merge.")
        return None


def compare_models(final_csv_paths):
    """
    Merge model outputs side-by-side for comparison.
    """
    dfs = []

    for model_key, path in final_csv_paths.items():
        if path is None or not os.path.exists(path):
            continue

        df = pd.read_csv(path, encoding="utf-8-sig", sep=";")
        keep_cols = ["id", "r", "b"]

        missing = [c for c in keep_cols if c not in df.columns]
        if missing:
            raise ValueError(f"[{model_key}] Missing columns in merged output: {missing}")

        df = df[keep_cols].copy()
        df = df.rename(columns={
            "r": f"r_{model_key}",
            "b": f"b_{model_key}",
        })
        dfs.append(df)

    if not dfs:
        print("No final CSVs available for model comparison.")
        return None

    comparison_df = dfs[0]
    for df_other in dfs[1:]:
        comparison_df = comparison_df.merge(df_other, on="id", how="outer")

    comparison_path = os.path.join(final_data_folder, "model_comparison.csv")
    comparison_df.to_csv(comparison_path, index=False, encoding="utf-8-sig", sep=";")
    print(f"Model comparison saved at: {comparison_path}")
    return comparison_path


